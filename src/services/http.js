/*
|--------------------------------------------------------------------------
| Import npm
|--------------------------------------------------------------------------
|
*/
import Axios from 'axios'
import Vue from 'vue'

/*
|--------------------------------------------------------------------------
| axios
|--------------------------------------------------------------------------
|
*/
let axios = Axios.create({
  timeout: 720000, // 720 segundos
  headers: { 'Authorization': 'Bearer ' + localStorage.getItem('id_token') }
})

/*
|--------------------------------------------------------------------------
| $http
|--------------------------------------------------------------------------
|
*/
let http = new Vue({
  data () {
    return {
      axios: axios
    }
  }
})

export default {
  install: function(Vue) {
    Vue.prototype.$http = http
  }
}
